    private void initCounterFlight() {
        Ivy bus = new Ivy("IvyControlPanel", "IvyControlPanel Ready", null);
        try {
            bus.start("10.3.8.255:1234");
        } catch (IvyException ex) {
            Logger.getLogger(ControlPanelFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            bus.bindMsg("TrackMovedEvent Flight=(.*) CallSign=AFR", new IvyMessageListener() {

                @Override
                public void receive(IvyClient ic, String[] strings) {
                    nbVolAirFrance++;
                    jLabel1.setText(Integer.toString(nbVolAirFrance));
                }
            });
        } catch (IvyException ex) {
            Logger.getLogger(ControlPanelFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

